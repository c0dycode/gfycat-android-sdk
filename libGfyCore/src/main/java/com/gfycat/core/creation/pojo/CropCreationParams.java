package com.gfycat.core.creation.pojo;

/**
 * Crop params mapped with json sended to server.
 * <p/>
 * Created by dekalo on 28.01.16.
 */
public class CropCreationParams {
    int x, y, w, h;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }
}
